<?php

namespace Debugmate\Commands;

use Debugmate\Exceptions\DebugmateErrorHandler;
use Exception;
use Symfony\Component\Console\Command\Command;
use Symfony\Component\Console\Input\InputInterface;
use Symfony\Component\Console\Logger\ConsoleLogger;
use Symfony\Component\Console\Output\OutputInterface;

class TestDebugmateCommand extends Command
{
    public function __construct()
    {
        parent::__construct('test');
        parent::setDescription('Send fake data to webhook');
    }

    protected function execute(InputInterface $input, OutputInterface $output)
    {
        $logger = new ConsoleLogger($output);

        if (!$link = getenv('DEBUGMATE_DOMAIN')) {
            $logger->error('You must fill DEBUGMATE_DOMAIN env with a valid debugmate endpoint');

            return Command::FAILURE;
        }

        /** @var DebugmateErrorHandler $errorHandler */
        $errorHandler = new DebugmateErrorHandler();
        $errorHandler->write([
            'context' => [
                'exception' => new Exception('Test generated by the debugmate:test command'),
            ],
        ]);

        if ($errorHandler->failed) {
            $logger->error('We couldn\'t reach Debugmate Server at ' . $link);

            if ($reason = $errorHandler->reason()) {
                $logger->error($reason);
            }

            return Command::FAILURE;
        }

        $output->writeln("<info>Debugmate reached successfully. We sent a test Exception that has been registered.</info>");

        return Command::SUCCESS;
    }
}
